    <script type="text/javascript">

        ////////////////////////////////////////////////////

        var training_photo = new KTImageInput('kt_training_photo');
        ////////////////////////////////////////////////////////////
        ///////// Datepicker
        $('#started_date').datepicker({
            format: "yyyy-mm-dd",
            todayBtn: true,
            clearBtn: false,
            orientation: "bottom auto",
            language: "{{LaravelLocalization::getCurrentLocale()}}",
            autoclose: true,
            todayHighlight: true,
        });


        $('#form_training_add').on('submit', function (e) {
            e.preventDefault();
            $.notifyClose();
            /////////////////////////////////////////////////////////////////////////
            $('#photo_error').text('');
            $('#title_ar_error').text('');
            $('#title_en_error').text('');
            $('#started_date_error').text('');

            $('#photo').css('border-color','');
            $('#title_ar').css('border-color','');
            $('#title_en').css('border-color','');
            $('#started_date').css('border-color','');
            ////////////////////////////////////////////////////////////////////////
            var data = new FormData(this);
            var url = $(this).attr('action');
            var type = $(this).attr('method');
            $.ajax({
                url: url,
                data: data,
                type: type,
                dataType: 'JSON',
                cache: false,
                processData: false,
                contentType: false,
                beforeSend: function () {
                    KTApp.blockPage({
                        overlayColor: '#000000',
                        state: 'danger',
                        message: "{{trans('general.please_wait')}}",
                    });
                    setTimeout(function () {
                        KTApp.unblockPage();
                    }, 1500);
                },

                success: function (data) {
                    console.log(data);
                    if (data.status == true) {
                        notifySuccessOrError(data.msg, 'success');
                        setTimeout(function (){
                            window.location.href = '{{route('admin.trainings')}}';
                        },2500)
                    }
                },


                error: function (reject) {
                    $.notifyClose();
                    var response = $.parseJSON(reject.responseText);
                    $.each(response.errors, function (key, value) {
                        $('#' + key + '_error').text(value[0]);
                        $('#' + key).css('border-color', 'red');
                        $('html,body').animate({scrollTop: 20}, 300);
                    });
                },
                complete: function () {
                    KTApp.unblockPage();
                }

            })
        });

    </script>
@endpush
